     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newUser = new User;
        $newUser->name = $request->name;
        $newUser->password = $request->password;
        $newUser->email = $request->email;
        $newUser->save();

        return $newUser;
    }

